# realiza un programa que pregunte por dos números
# y realice la suma de los mismos, mostrar resultado
# entrada -> proceso -> salida
# entrada
print("Ingresa un número entero")
numero1 = int(input())
print("Ingresa otro número entero")
numero2 = int(input())
# proceso
resultado = numero1 + numero2
# salida, concatenación
print("La suma de",numero1,"+",numero2,"=",resultado)

# realiza un programa para calcular el área
# de un triángulo, fórmula (base * altura)/2
# entrada
print("Ingresa el valor de la base del triángulo")
base = float(input())
print("Ingresa el valor de la altura del triángulo")
altura = float(input())
# proceso
resultado = (base * altura)/2
# salida
print("El área del triángulo es", resultado)
